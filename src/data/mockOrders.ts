/** Build a Date offset by `daysAgo` days, at a specific hour/minute. */
function daysAgo(days: number, hour = 9, minute = 0): Date {
  const d = new Date();
  d.setDate(d.getDate() - days);
  d.setHours(hour, minute, 0, 0);
  return d;
}

let _idCounter = 1000;
function nextId(): string {
  return `order-${(_idCounter++).toString().padStart(6, '0')}`;
}

function nextOrderNum(prefix: string): string {
  return `${prefix}-${Math.floor(10000 + Math.random() * 90000)}`;
}

function nextOrderId(): number {
  return Math.floor(1_000_000 + Math.random() * 9_000_000);
}

// ─────────────────────────────────────────────────────────────────────────────
// Label factory
// ─────────────────────────────────────────────────────────────────────────────

function makeLabel(
  trackingNumber: string,
  shipmentCost: number,
  carrierCode: string,
  serviceCode: string,
  createdAt: Date,
): OrderLabel {
  return {
    trackingNumber,
    shipmentCost,
    v2CarrierCode: carrierCode,
    serviceCode,
    labelUrl: `https://labels.shipstation.com/${trackingNumber}.pdf`,
    v1ShippingProviderId: 45893,
    v1CarrierCode: carrierCode.toUpperCase(),
    createdAt,
    createdBy: 'user-admin',
    voided: false,
  };
}

// ─────────────────────────────────────────────────────────────────────────────
// Billing factory
// ─────────────────────────────────────────────────────────────────────────────

function makeBilling(
  baseRate: number,
  residential: boolean,
  markupPct: number,
): BillingCalculation {
  const residentialSurcharge = residential ? 0.2 : 0;
  const subtotal = baseRate + residentialSurcharge;
  const totalCost = Math.round(subtotal * (1 + markupPct) * 100) / 100;
  const breakdown = `$${baseRate.toFixed(2)}${residential ? ` + $${residentialSurcharge.toFixed(2)} res` : ''} + ${(markupPct * 100).toFixed(0)}% markup`;
  return {
    baseRate,
    residentialSurcharge,
    carrierMarkupPercent: markupPct,
    subtotal,
    totalCost,
    breakdown,
    calculatedAt: new Date(),
    roundingMethod: 'bankers',
  };
}

