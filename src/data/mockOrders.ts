export const MOCK_AWAITING = MOCK_ORDERS.filter(
  (o) => o.status === 'awaiting_shipment',
);

/** All shipped mock orders. */
export const MOCK_SHIPPED = MOCK_ORDERS.filter(
  (o) => o.status === 'shipped',
);

/** All cancelled mock orders. */
export const MOCK_CANCELLED = MOCK_ORDERS.filter(
  (o) => o.status === 'cancelled',
);

/** Get mock orders for a specific client. */
export function getMockOrdersByClient(clientId: string): Order[] {
  return MOCK_ORDERS.filter((o) => o.clientId === clientId);
}

/** Get mock orders by status. */
export function getMockOrdersByStatus(status: Order['status']): Order[] {
  return MOCK_ORDERS.filter((o) => o.status === status);
}
